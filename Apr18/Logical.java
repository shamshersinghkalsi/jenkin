package Apr18;

public class Logical {
    public static void main(String[] args) {
        int x = 100;
        int y = 98;
        boolean a = x<y;
        boolean b = x>y;
        System.out.println(a && b);
        System.out.println(a || b);
        System.out.println(!a);
        System.out.println(!b);
    }
}

