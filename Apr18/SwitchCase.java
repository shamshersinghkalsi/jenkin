package Apr18;

public class SwitchCase {
    public static void main(String[] args) {
        String day = "Monday";
        switch (day){
            case "Monday":
                System.out.println(" today is Monday");
                break;
            case "Sunday":
                    System.out.println(" today is Sunday");
                    break;
            default:
                System.out.println(" today is holiday");

        }
    }
}
